export const getMunicipalitiesByDepartment = (departmentId: string): Municipality[] => {
  return municipalities.filter(m => m.departmentId === departmentId);
};

export const getDepartmentById = (id: string): Department | undefined => {
  return departments.find(d => d.id === id);
};

export const getMunicipalityById = (id: string): Municipality | undefined => {
  return municipalities.find(m => m.id === id);
};

export const getDepartmentName = (departmentId: string): string => {
  const dept = departments.find(d => d.id === departmentId);
  return dept?.name || '';
};

export const getMunicipalityName = (municipalityId: string): string => {
  const muni = municipalities.find(m => m.id === municipalityId);
  return muni?.name || '';
};
